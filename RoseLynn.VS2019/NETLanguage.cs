namespace RoseLynn;

/// <summary>Represents a .NET language.</summary>
public enum NETLanguage
{
    /// <summary>Denotes that the language is an unknown language running in the .NET platform.</summary>
    Unknown = -1,

    /// <summary>Denotes the C# language.</summary>
    CSharp,
    /// <summary>Denotes the F# language.</summary>
    FSharp,
    /// <summary>Denotes the Visual Basic language.</summary>
    VisualBasic,
}
